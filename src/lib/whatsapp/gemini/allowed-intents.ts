export function normalizeGeminiIntent(intent: string) {
  const normalized = String(intent || "").trim().toUpperCase();
  return (GEMINI_ALLOWED_INTENTS as readonly string[]).includes(normalized) ? normalized : "";
}

export function mapGeminiIntentToRancho(intent: string): RanchoIntent | null {
  return GEMINI_TO_RANCHO_INTENT[normalizeGeminiIntent(intent)] || null;
}

